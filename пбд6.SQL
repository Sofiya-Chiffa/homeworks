--
-- УРОВЕНЬ 1. НЕЗАВИСИМЫЕ СПРАВОЧНИКИ
--

CREATE TABLE country (
  country_id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  code varchar(3) NOT NULL UNIQUE,
  name varchar(100) NOT NULL UNIQUE
);


CREATE TABLE position (
  position_id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name varchar(100) NOT NULL UNIQUE,
  base_salary numeric(10,2) NOT NULL
);


CREATE TABLE bbc_section (
  bbc_section_id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  code varchar(20) NOT NULL UNIQUE,
  name varchar(200) NOT NULL UNIQUE
);


CREATE TABLE event_type (
  event_type_id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name varchar(100) NOT NULL UNIQUE,
  default_duration smallint NOT NULL DEFAULT 60
);


CREATE TABLE location (
  location_id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  zone varchar(50) NOT NULL,
  bookcase smallint NOT NULL,
  shelf smallint NOT NULL,
  max_capacity integer NOT NULL DEFAULT 100
);

--
-- УРОВЕНЬ 2. ЗАВИСИМЫЕ СПРАВОЧНИКИ
--

CREATE TABLE publisher (
  publisher_id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name varchar(200) NOT NULL UNIQUE,
  country_id integer NOT NULL
);

--
-- УРОВЕНЬ 3. КЛЮЧЕВЫЕ ОБЪЕКТЫ
--

CREATE TABLE employee (
  employee_id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  first_name varchar(100) NOT NULL,
  last_name varchar(100) NOT NULL,
  middle_name varchar(100),
  position_id integer NOT NULL,
  login varchar(50) NOT NULL UNIQUE,
  password varchar(255) NOT NULL,
  email varchar(100) NOT NULL UNIQUE,
  hire_date date NOT NULL DEFAULT CURRENT_DATE,
  is_active boolean NOT NULL DEFAULT true
);


CREATE TABLE reader (
  reader_id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  library_card_number varchar(20) NOT NULL UNIQUE,
  first_name varchar(100) NOT NULL,
  last_name varchar(100) NOT NULL,
  middle_name varchar(100),
  birth_date date NOT NULL,
  gender char(1) NOT NULL,
  address varchar(255) NOT NULL,
  email varchar(100) UNIQUE,
  reg_date date NOT NULL DEFAULT CURRENT_DATE,
  is_active boolean NOT NULL DEFAULT true
);


CREATE TABLE book_item (
  book_item_id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  title varchar(255) NOT NULL,
  publisher_id integer NOT NULL,
  publish_year smallint NOT NULL,
  pages integer NOT NULL,
  inventory_number varchar(50) NOT NULL UNIQUE,
  isbn varchar(20),
  bbc_section_id integer NOT NULL,
  status varchar(20) NOT NULL DEFAULT 'В наличии',
  location_id integer NOT NULL
);


CREATE TABLE author (
  author_id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  first_name varchar(100) NOT NULL,
  last_name varchar(100) NOT NULL,
  middle_name varchar(100),
  birth_date date NOT NULL,
  death_date date,
  country_id integer NOT NULL
);

--
-- УРОВЕНЬ 4. ДОКУМЕНТЫ И РЕГИСТРЫ
--

CREATE TABLE event (
  event_id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name varchar(100) NOT NULL,
  event_date timestamp NOT NULL,
  event_type_id integer NOT NULL,
  employee_id integer NOT NULL,
  location varchar(100) NOT NULL,
  description text,
  status varchar(20) NOT NULL DEFAULT 'Планируется'
);


CREATE TABLE checkout (
  checkout_id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  checkout_date date NOT NULL DEFAULT CURRENT_DATE,
  reader_id integer NOT NULL,
  employee_id integer NOT NULL,
  planned_return_date date NOT NULL DEFAULT (CURRENT_DATE + INTERVAL '30 days'),
  status varchar(20) NOT NULL DEFAULT 'Активна'
);
COMMENT ON COLUMN checkout.status IS 'Статус выдачи: "Активна" или "Закрыта"';


CREATE TABLE supply (
  supply_id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  supply_date date NOT NULL DEFAULT CURRENT_DATE,
  supplier varchar(200) NOT NULL,
  employee_id integer NOT NULL,
  invoice_number varchar(50) NOT NULL UNIQUE
);


CREATE TABLE write_off (
  write_off_id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  write_off_date date NOT NULL DEFAULT CURRENT_DATE,
  employee_id integer NOT NULL,
  act_number varchar(50) NOT NULL UNIQUE
);
COMMENT ON TABLE write_off IS 'Акт списания ветхих, утерянных или испорченных книг. Содержит номер акта, дату и сотрудника, оформившего списание.';

--
-- УРОВЕНЬ 5. ПРОМЕЖУТОЧНЫЕ ТАБЛИЦЫ (позиции документов и связи M:N)
--

CREATE TABLE checkout_item (
  checkout_id integer NOT NULL,
  book_item_id integer NOT NULL,
  actual_return_date date,
  fine_amount numeric(10,2),
  PRIMARY KEY (checkout_id, book_item_id)
);
COMMENT ON TABLE checkout_item IS 'Cписок книг, выданных читателю в рамках одного чека, с информацией о возврате и штрафах';

COMMENT ON COLUMN checkout_item.actual_return_date IS 'Дата фактического возврата книги (NULL означает, что книга ещё на руках у читателя)';


CREATE TABLE supply_item (
  supply_id integer NOT NULL,
  book_item_id integer NOT NULL,
  price numeric(10,2) NOT NULL,
  PRIMARY KEY (supply_id, book_item_id)
);


CREATE TABLE write_off_item (
  write_off_id integer NOT NULL,
  book_item_id integer NOT NULL,
  reason text NOT NULL,
  reader_id integer,
  compensation_status varchar(20),
  PRIMARY KEY (write_off_id, book_item_id)
);


CREATE TABLE employee_phone (
  employee_phone_id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  employee_id integer NOT NULL,
  phone varchar(20) NOT NULL
);


CREATE TABLE reader_phone (
  reader_phone_id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  reader_id integer NOT NULL,
  phone varchar(20) NOT NULL
);


CREATE TABLE book_author (
  author_id integer NOT NULL,
  book_item_id integer NOT NULL,
  PRIMARY KEY (author_id, book_item_id)
);

--
-- БЛОК ОГРАНИЧЕНИЙ ВНЕШНИХ КЛЮЧЕЙ (С ПРАВИЛАМИ ON DELETE / ON UPDATE)
--
-- Защита справочников: объекты зависят от справочников (ON DELETE RESTRICT)
ALTER TABLE publisher ADD CONSTRAINT publisher_country_id_fk FOREIGN KEY (country_id) REFERENCES country (country_id) ON DELETE RESTRICT;
ALTER TABLE author ADD CONSTRAINT author_country_id_fk FOREIGN KEY (country_id) REFERENCES country (country_id) ON DELETE RESTRICT;
ALTER TABLE employee ADD CONSTRAINT employee_position_id_fk FOREIGN KEY (position_id) REFERENCES position (position_id) ON DELETE RESTRICT;
ALTER TABLE event ADD CONSTRAINT event_event_type_id_fk FOREIGN KEY (event_type_id) REFERENCES event_type (event_type_id) ON DELETE RESTRICT;
ALTER TABLE book_item ADD CONSTRAINT book_item_publisher_id_fk FOREIGN KEY (publisher_id) REFERENCES publisher (publisher_id) ON DELETE RESTRICT;
ALTER TABLE book_item ADD CONSTRAINT book_item_bbc_section_id_fk FOREIGN KEY (bbc_section_id) REFERENCES bbc_section (bbc_section_id) ON DELETE RESTRICT;
ALTER TABLE book_item ADD CONSTRAINT book_item_location_id_fk FOREIGN KEY (location_id) REFERENCES location (location_id) ON DELETE RESTRICT;

-- Зависимые атрибуты объектов: телефоны исчезают вместе с владельцем (ON DELETE CASCADE)
ALTER TABLE employee_phone ADD CONSTRAINT employee_phone_employee_id_fk FOREIGN KEY (employee_id) REFERENCES employee (employee_id) ON DELETE CASCADE;
ALTER TABLE reader_phone ADD CONSTRAINT reader_phone_reader_id_fk FOREIGN KEY (reader_id) REFERENCES reader (reader_id) ON DELETE CASCADE;

-- Историческая безопасность: документы блокируют удаление участников сделок (ON DELETE RESTRICT)
ALTER TABLE checkout ADD CONSTRAINT checkout_employee_id_fk FOREIGN KEY (employee_id) REFERENCES employee (employee_id) ON DELETE RESTRICT;
ALTER TABLE checkout ADD CONSTRAINT checkout_reader_id_fk FOREIGN KEY (reader_id) REFERENCES reader (reader_id) ON DELETE RESTRICT;
ALTER TABLE supply ADD CONSTRAINT supply_employee_id_fk FOREIGN KEY (employee_id) REFERENCES employee (employee_id) ON DELETE RESTRICT;
ALTER TABLE write_off ADD CONSTRAINT write_off_employee_id_fk FOREIGN KEY (employee_id) REFERENCES employee (employee_id) ON DELETE RESTRICT;
ALTER TABLE write_off_item ADD CONSTRAINT write_off_item_reader_id_fk FOREIGN KEY (reader_id) REFERENCES reader (reader_id) ON DELETE RESTRICT;

-- Целостность документов: промежуточные таблицы жёстко зависят от документов (ON DELETE CASCADE)
ALTER TABLE checkout_item ADD CONSTRAINT checkout_item_checkout_id_fk FOREIGN KEY (checkout_id) REFERENCES checkout (checkout_id) ON DELETE CASCADE ON UPDATE CASCADE;
ALTER TABLE supply_item ADD CONSTRAINT supply_item_supply_id_fk FOREIGN KEY (supply_id) REFERENCES supply (supply_id) ON DELETE CASCADE ON UPDATE CASCADE;
ALTER TABLE write_off_item ADD CONSTRAINT write_off_item_write_off_id_fk FOREIGN KEY (write_off_id) REFERENCES write_off (write_off_id) ON DELETE CASCADE ON UPDATE CASCADE;
ALTER TABLE book_author ADD CONSTRAINT book_author_book_item_id_fk FOREIGN KEY (book_item_id) REFERENCES book_item (book_item_id) ON DELETE CASCADE ON UPDATE CASCADE;
ALTER TABLE book_author ADD CONSTRAINT book_author_author_id_fk FOREIGN KEY (author_id) REFERENCES author (author_id) ON DELETE CASCADE ON UPDATE CASCADE;

ALTER TABLE event ADD CONSTRAINT event_employee_id_fk FOREIGN KEY (employee_id) REFERENCES employee (employee_id) ON DELETE RESTRICT;
ALTER TABLE checkout_item ADD CONSTRAINT checkout_item_book_item_id_fk FOREIGN KEY (book_item_id) REFERENCES book_item (book_item_id) ON DELETE RESTRICT;
ALTER TABLE write_off_item ADD CONSTRAINT write_off_item_book_item_id_fk FOREIGN KEY (book_item_id) REFERENCES book_item (book_item_id) ON DELETE RESTRICT;
ALTER TABLE supply_item ADD CONSTRAINT supply_item_book_item_id_fk FOREIGN KEY (book_item_id) REFERENCES book_item (book_item_id) ON DELETE RESTRICT;
